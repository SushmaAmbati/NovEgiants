package ItemOrder;

public class Address {
	public String aptNo;
    public String street;
    public String city;
    public String state;
    public String zipcode;
    public String country;
    
    
  public Address(String aptNo, String street, String city, String state, String zipcode, String country) {
    	this.aptNo = aptNo;
    	this.street = street;
    	this.city = city;
    	this.state = state;
    	this.zipcode = zipcode;
    	this.country = country;
    }

}
