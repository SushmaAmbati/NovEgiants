package ItemOrder;

public class Company {
	public String name;
	public String street;
	public String city;
	public String state;
	public String zipcode;
	public String country;
	
	 public Company(String name, String street, String city, String state, String zipcode, String country) {
	    	this.name = name;
	    	this.street = street;
	    	this.city = city;
	    	this.state = state;
	    	this.zipcode = zipcode;
	    	this.country = country;
	    }

}
